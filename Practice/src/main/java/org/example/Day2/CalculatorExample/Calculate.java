package org.example.Day2.CalculatorExample;

public class Calculate {
    public float add(float oper1, float oper2){
        float sum = oper1 + oper2;
        return sum;
    }

    public float subtract(float oper1, float oper2){
        float difference = oper1 - oper2;
        return difference;
    }

    public float multiply(float oper1, float oper2){
        float product = oper1 * oper2;
        return product;
    }

    public float divide(float oper1, float oper2){
        float quotient = oper1 / oper2;
        return quotient;
    }
}
